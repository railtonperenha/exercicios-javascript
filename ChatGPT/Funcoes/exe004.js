// 4. Verificação de Par ou Ímpar
// Escreva uma função chamada verificaPar que recebe um número como parâmetro e retorna true se o número for par e false se for ímpar.


function verificaPar (numero) {
    return numero % 2 === 0;
};

console.log(verificaPar(8)); // Saída vai retornar um boolean: true ou false
