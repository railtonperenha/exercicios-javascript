// Verificar velocidade permitida
// Com base na velocidade de um carro armazenada na variável velocidade, retorne "Dentro do limite" se for menor ou igual a 80 km/h ou "Acima do limite" caso contrário.

let velocidade = 100;
if (velocidade <= 80) {
    console.log('Dentro do limite!');
} else {
    console.log('Acima do limite!');
};